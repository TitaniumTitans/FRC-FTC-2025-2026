package org.firstinspires.ftc.teamcode;

import com.qualcomm.robotcore.eventloop.opmode.OpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.qualcomm.robotcore.hardware.DcMotor;
import com.qualcomm.robotcore.util.ElapsedTime;


/*
 * This file contains an example of an iterative (Non-Linear) "OpMode".
 * An OpMode is a 'program' that runs in either the autonomous or the teleop period of an FTC match.
 * The names of OpModes appear on the menu of the FTC Driver Station.
 * When a selection is made from the menu, the corresponding OpMode
 * class is instantiated on the Robot Controller and executed.
 *
 * This particular OpMode just executes a basic Tank Drive Teleop for a two wheeled robot
 * It includes all the skeletal structure that all iterative OpModes contain.
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this OpMode to the Driver Station OpMode list
 */

@TeleOp(name="Teleop Opmode", group="Iterative OpMode")
public class TeleopOpmode extends OpMode
{
    // Declare OpMode members.
    private ElapsedTime runtime = new ElapsedTime();
    private DcMotor frontLeftDrive = null;
    private DcMotor frontRightDrive = null;
    private DcMotor backLeftDrive = null;
    private DcMotor backRightDrive = null;

    private DcMotor intake = null;
    private DcMotor kicker = null;
    private DcMotor topShooter = null;
    private DcMotor bottomShooter = null;

    /*
     * Code to run ONCE when the driver hits INIT
     */
    @Override
    public void init() {
        telemetry.addData("Status", "Initialized");

        // Initialize the hardware variables. Note that the strings used here as parameters
        // to 'get' must correspond to the names assigned during the robot configuration
        // step (using the FTC Robot Controller app on the phone).
        frontLeftDrive = hardwareMap.get(DcMotor.class, "front_left_drive");
        frontRightDrive = hardwareMap.get(DcMotor.class, "front_right_drive");
        backLeftDrive = hardwareMap.get(DcMotor.class, "back_left_drive");
        backRightDrive = hardwareMap.get(DcMotor.class, "back_right_drive");

        intake = hardwareMap.get(DcMotor.class, "intake");
        kicker = hardwareMap.get(DcMotor.class, "kicker");
        topShooter = hardwareMap.get(DcMotor.class, "top_shooter");
        bottomShooter = hardwareMap.get(DcMotor.class, "bottom_shooter");


        // To drive forward, most robots need the motor on one side to be reversed, because the axles point in opposite directions.
        // Pushing the left stick forward MUST make robot go forward. So adjust these two lines based on your first test drive.
        // Note: The settings here assume direct drive on left and right wheels.  Gear Reduction or 90 Deg drives may require direction flips
        frontLeftDrive.setDirection(DcMotor.Direction.REVERSE);
        frontRightDrive.setDirection(DcMotor.Direction.FORWARD);

        // Tell the driver that initialization is complete.
        telemetry.addData("Status", "Initialized");
    }

    /*
     * Code to run REPEATEDLY after the driver hits INIT, but before they hit START
     */
    @Override
    public void init_loop() {
    }

    /*
     * Code to run ONCE when the driver hits START
     */
    @Override
    public void start() {
        runtime.reset();
    }

    /*
     * Code to run REPEATEDLY after the driver hits START but before they hit STOP
     */
    @Override
    public void loop() {
        // Drivetrain code
        double x = gamepad1.left_stick_x;
        double rx = gamepad1.right_stick_x;
        double y = -gamepad1.left_stick_y;

        double setPoint = 1;
        double error = setPoint - bottomShooter.getPower();
        double output = error * 1;


        frontLeftDrive.setPower(y + x + rx);
        backLeftDrive.setPower(y - x + rx);
        frontRightDrive.setPower(y - x - rx);
        backRightDrive.setPower(y + x - rx);

        // shooter
        if (gamepad1.x) {
            bottomShooter.setPower(output);
            topShooter.setPower(output);
        } else {
            bottomShooter.setPower(0);
            topShooter.setPower(0);
        }

        // intake
        if (gamepad1.a) {
            intake.setPower(1);
        } else {
            intake.setPower(0);
        }

        // kicker
        if (gamepad1.b) {
            kicker.setPower(1);
        } else {
            kicker.setPower(0);
        }
    }
    /*
     * Code to run ONCE after the driver hits STOP
     */
    @Override
    public void stop() {
    }

}
